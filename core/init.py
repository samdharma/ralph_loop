def _create_labels(repo: str) -> tuple[int, int]:
    """Create all Ralph labels on the GitHub repo.
    Returns (created, skipped) counts."""
    import json

    # Get existing labels
    try:
        result = subprocess.run(
            ["gh", "label", "list", "--repo", repo, "--json", "name"],
            capture_output=True,
            text=True,
        )
        existing = {label["name"] for label in json.loads(result.stdout)}
    except Exception:
        existing = set()

    created = 0
    skipped = 0
    for name, color, desc in RALPH_LABELS:
        if name in existing:
            skipped += 1
            continue
        try:
            subprocess.run(
                [
                    "gh",
                    "label",
                    "create",
                    name,
                    "--color",
                    color,
                    "--description",
                    desc,
                    "--repo",
                    repo,
                ],
                capture_output=True,
                check=True,
            )
            created += 1
            print(f"  ✓  {name}")
        except subprocess.CalledProcessError as e:
            print(f"  ✗  {name} — {e.stderr.strip() if e.stderr else 'failed'}")

    return created, skipped


