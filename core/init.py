def _detect_project_name(project_dir: Path) -> str:
    """Infer a project name from the directory name."""
    name = project_dir.resolve().name
    # Sanitize: lowercase, replace spaces/special chars with hyphens
    import re

    name = re.sub(r"[^a-zA-Z0-9_-]", "-", name).strip("-")
    return name or "my-project"


def _extract_repo_from_url(url: str) -> str | None:
    """Extract owner/repo from a git remote URL."""
    for prefix in ["https://github.com/", "git@github.com:"]:
        if url.startswith(prefix):
            path = url[len(prefix) :]
            if path.endswith(".git"):
                path = path[:-4]
            return path
    return None


def _detect_repo(project_dir: Path) -> str:
    """Try to detect the GitHub repo from git remote."""
    try:
        result = subprocess.run(
            ["git", "remote", "get-url", "origin"],
            capture_output=True,
            text=True,
            cwd=project_dir,
        )
        if result.returncode == 0:
            repo = _extract_repo_from_url(result.stdout.strip())
            if repo:
                return repo
    except Exception:
        pass
    return "owner/repo"


def _detect_agent() -> str:
    """Check which AI agents are available."""
    for agent in ["pi", "kimi"]:
        if subprocess.run(["which", agent], capture_output=True).returncode == 0:
            return agent
    return "pi"  # default


def _prompt(prompt: str, default: str, yes: bool = False) -> str:
    """Ask the user a question, return their answer or the default."""
    if yes:
        print(f"{prompt} [{default}]: {default}")
        return default
    try:
        answer = input(f"{prompt} [{default}]: ").strip()
        return answer if answer else default
    except (EOFError, KeyboardInterrupt):
        print()
        return default


def _confirm(prompt: str, default: bool = True, yes: bool = False) -> bool:
    """Ask a yes/no question. Returns True/False."""
    yn = "Y/n" if default else "y/N"
    if yes:
        print(f"{prompt} [{yn}]: {'Y' if default else 'N'}")
        return default
    try:
        answer = input(f"{prompt} [{yn}]: ").strip().lower()
        if not answer:
            return default
        return answer in ("y", "yes")
    except (EOFError, KeyboardInterrupt):
        print()
        return default


def _init_git(project_dir: Path):
    """Initialize a git repository and commit the scaffold."""
    try:
        subprocess.run(
            ["git", "init"], cwd=project_dir, capture_output=True, check=True
        )
        subprocess.run(
            ["git", "add", "-A"], cwd=project_dir, capture_output=True, check=True
        )
        subprocess.run(
            ["git", "commit", "-m", "ralph init"],
            cwd=project_dir,
            capture_output=True,
            check=True,
        )
        print("  ✓  git init && git commit 'ralph init'")
    except subprocess.CalledProcessError as e:
        print(f"  ⚠  git init failed: {e.stderr.strip() if e.stderr else e}")
        print("     You can initialize git manually:")
        print("       git init && git add -A && git commit -m 'ralph init'")


